SET tongChiTieu = (
    SELECT IFNULL(SUM(hd.tongTien), 0)
    FROM HoaDon hd
    INNER JOIN DonDatMon ddm ON hd.maDon = ddm.maDon
    WHERE ddm.maKH = KhachHang.maKH
    AND hd.trangThai = 'Đã thanh toán'
);

